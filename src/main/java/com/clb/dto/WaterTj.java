package com.clb.dto;

public class WaterTj {

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public float getVol() {
        return vol;
    }

    public void setVol(float vol) {
        this.vol = vol;
    }

    public float getArea() {
        return area;
    }

    public void setArea(float area) {
        this.area = area;
    }

    public float getLength() {
        return length;
    }

    public void setLength(float length) {
        this.length = length;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    private String type;
    private float vol;
    private float area;
    private float length;
    private int number;

}
