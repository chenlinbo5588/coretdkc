package com.clb.dto;

public class TubiaoData {

        private String name;
        private  float value;
        private float zb;
        private int number;
        private String type;
        private String numberDw;
        private String dw;

    public String getDw() {
        return dw;
    }

    public void setDw(String dw) {
        this.dw = dw;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getNumberDw() {
        return numberDw;
    }

    public void setNumberDw(String numberDw) {
        this.numberDw = numberDw;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public float getZb() {
        return zb;
    }

    public void setZb(float zb) {
        this.zb = zb;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getValue() {
        return value;
    }

    public void setValue(float value) {
        this.value = value;
    }
}
